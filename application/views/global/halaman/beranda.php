<h1>Selamat Datang di Kharisma Labuan Beach and Resort</h1>
<p>Lokasi resort tepi pantai dengan berbagai fasilitas yang dapat dijadikan tujuan/pilihan untuk melakukan kegiatan-kegiatan seperti family gathering, meeting, training, outing atau hanya one day trip. Hotel Kharisma, Beach and Resort, Labuan didirikan pada bulan Maret 1993, dioperasionalkan oleh PT. Kharisma Labuan Wisata yang merupakan salah satu unit usaha "PT. Kharisma Group", dengan bentuk usaha perhotelan yang berada pada daerah pantai Labuan, Pandeglang, Propinsi Banten.</p>
<p>SitePlan yang dibangun untuk terciptanya Sangraloka Terbaik Untuk Keluarga ini dapat ditempuh hanya 2,5 jam melalui tol Jakarta-Merak, baik melalui Cilegon ataupun Pandeglang. Hotel Kharisma Labuan, Beach and Resort berlokasi di tepi pantai Bama dengan hamparan pasir putih nan landai dan tidak berkarang yang merupakan pantai terbaik di Labuan tepatnya berada di jalan Raya Panimbang.</p>

